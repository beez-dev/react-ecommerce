import mongoose from "mongoose";
import { USER_MODEL_NAME } from "../constants/constants.js";

const userSchema = mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
    isAdmin: {
      type: Boolean,
      required: true,
      default: false,
    },
  },
  { timestamps: true }
);

const UserModel = mongoose.model(USER_MODEL_NAME, userSchema);

export default UserModel;
